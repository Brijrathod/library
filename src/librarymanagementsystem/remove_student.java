    private void removebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removebtnActionPerformed
        
                        int selectedRow=jTable1.getSelectedRow();
                        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
                    
                    if(selectedRow>=0)
                    {
                        int confirmation=JOptionPane.showConfirmDialog(null,"Are You Sure The Delete Student","Confirmation",JOptionPane.YES_NO_OPTION);
                        
                        if(confirmation==JOptionPane.YES_OPTION)
                        {
                            String studentid=model.getValueAt(selectedRow,0).toString();
                            
                            try{
                                
                                try {
                                    Class.forName("com.mysql.cj.jdbc.Driver");
                                } catch (ClassNotFoundException ex) {
                                    Logger.getLogger(remove_book.class.getName()).log(Level.SEVERE, null, ex);
                                }
                                    Connection con =java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","");
                                    Statement st=con.createStatement();
                                
                                    String queary="delete from student where student_id=?";
                                    java.sql.PreparedStatement statement=con.prepareStatement(queary);
                                    statement.setString(1,studentid);
                                    int rowsDeleted=statement.executeUpdate();
                                    
                                    if(rowsDeleted >0)
                                    {
                                        JOptionPane.showMessageDialog(this,"Student Deleted Successfully");
                                        model.removeRow(selectedRow);
                                        
                                        student_name.setText("");
                                        book_name.setText("");
                                    }
                            }
                            catch(SQLException e)
                            {
                                System.out.println(e.getMessage());
                            }
                        }
                    }
    }//GEN-LAST:event_removebtnActionPerformed

