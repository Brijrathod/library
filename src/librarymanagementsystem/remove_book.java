    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed
        
                DefaultTableModel model=(DefaultTableModel)jTable2.getModel();
                int notFount=0;
            
                if("".equals(book_name.getText())) 
                {
                    JOptionPane.showMessageDialog(this,"Book Name is require ");
                }
                else if("".equals(author_name.getText()))
                {
                    JOptionPane.showMessageDialog(this,"Author Name is require");
                }
                else
                {
                    try
                    {
                        
                        Class.forName("com.mysql.cj.jdbc.Driver");
                        Connection con =DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","");
                        Statement st=con.createStatement();
                        
                        String Book_name=book_name.getText();
                        String Author_name=author_name.getText();
                
                        
                         String queary=("select * from books where book_name=? and book_author=? ");
                         PreparedStatement pst=con.prepareStatement(queary);
                         
                         pst.setString(1,Book_name);
                         pst.setString(2,Author_name);
                         
                         ResultSet rs=pst.executeQuery();
                        // PreparedStatement ps=con.prepareStatement("select * from books where book_name='"+Book_name+"' and book_author='"+Author_name+"'");
                        //ResultSet rs=(ResultSet) ps.executeQuery();
                
                
                        if(rs.next())
                        {
                            String Book_id=rs.getString("book_id");
                             String Category=rs.getString("book_category");
                             String Book_Name=rs.getString("book_name");
                             String Author=rs.getString("book_author");
                             int Copies=rs.getInt("book_copies");
                             
                             model.addRow(new Object[]{Book_id,Category,Book_Name,Author,Copies});
                          
                            
                            notFount=1;
                        }
                        if(notFount==0){
                            JOptionPane.showMessageDialog(this," Values is not found");
                        }
                        
                        
                        rs.close();
                        pst.close();
                        con.close();
                    }
                    catch(ClassNotFoundException | SQLException e)
                    {
                        System.out.println(e.getMessage());
                    }
                }
    }//GEN-LAST:event_searchbtnActionPerformed

