    private void removebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removebtnActionPerformed
                    
                    int selectedRow=jTable2.getSelectedRow();
                    DefaultTableModel model=(DefaultTableModel)jTable2.getModel();
                    
                    if(selectedRow>=0)
                    {
                        int confirmation=JOptionPane.showConfirmDialog(null,"Are You Sure The Delete Book","Confirmation",JOptionPane.YES_NO_OPTION);
                        
                        if(confirmation==JOptionPane.YES_OPTION)
                        {
                            String bookid=model.getValueAt(selectedRow,0).toString();
                            
                            try{
                                
                                try {
                                    Class.forName("com.mysql.cj.jdbc.Driver");
                                } catch (ClassNotFoundException ex) {
                                    Logger.getLogger(remove_book.class.getName()).log(Level.SEVERE, null, ex);
                                }
                                    Connection con =DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","");
                                    Statement st=con.createStatement();
                                
                                    String queary="delete from books where book_id=?";
                                    PreparedStatement statement=con.prepareStatement(queary);
                                    statement.setString(1,bookid);
                                    int rowsDeleted=statement.executeUpdate();
                                    
                                    if(rowsDeleted >0)
                                    {
                                        JOptionPane.showMessageDialog(this,"Book Deleted Successfully");
                                        model.removeRow(selectedRow);
                                        
                                        book_name.setText("");
                                        author_name.setText("");
                                    }
                            }
                            catch(SQLException e)
                            {
                                System.out.println(e.getMessage());
                            }
                        }
                    }
    }//GEN-LAST:event_removebtnActionPerformed

